package strategyPattern;

public class unit{
    String name;
    int hp;
    int speed;
    int power;
    int armor;

    unit(String name, int hp, int speed, int power, int armor){
        this.name = name;
        this.hp = hp;
        this.speed = speed;
        this.power = power;
        this.armor = armor;
    }
}